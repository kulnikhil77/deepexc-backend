# HYDRAULIC UPLIFT / PIPING
# ═══════════════════════════════════════════════════════════════
def _sec_hydraulic(doc, ch, gwt_behind, gwt_front, exc_depth, wall_toe, layers):
    doc.add_heading(f'{ch}. Hydraulic Uplift & Piping Check', level=1)
    _ref(doc, "Ref: IS 14458 Cl. 5.4 — Hydraulic stability; Terzaghi & Peck")

    gamma_w = 9.81
    doc.add_heading(f'{ch}.1 Piping Safety Factor', level=2)
    _p(doc, "Piping occurs when upward seepage force at the excavation base exceeds "
       "the submerged weight of the soil prism below the wall toe.")
    _f(doc, "FOS_piping = (gamma_sub x D) / (gamma_w x Delta_h)")
    _p(doc, "Where:")
    _f(doc, "D = embedment below excavation")
    _f(doc, "Delta_h = differential water head")
    _f(doc, "gamma_sub = gamma_sat - gamma_w")

    D = wall_toe - exc_depth
    delta_h = abs(gwt_front - gwt_behind)
    # Find gamma at embedment zone
    gamma_emb = 20.0
    for L in layers:
        if L.get('top', 0) <= exc_depth < L.get('bottom', 99):
            gamma_emb = L.get('gamma', 20.0)
    gamma_sub = gamma_emb - gamma_w

    _f(doc, f"D = {wall_toe:.1f} - {exc_depth:.1f} = {D:.1f} m")
    _f(doc, f"Delta_h = |{gwt_front:.1f} - {gwt_behind:.1f}| = {delta_h:.1f} m")
    _f(doc, f"gamma_sub = {gamma_emb:.1f} - {gamma_w} = {gamma_sub:.1f} kN/m3")

    if delta_h > 0:
        fos_p = gamma_sub * D / (gamma_w * delta_h)
        _f(doc, f"FOS = {gamma_sub:.1f} x {D:.1f} / ({gamma_w} x {delta_h:.1f}) = {fos_p:.2f}")
        _result(doc, "Piping FOS", f"{fos_p:.2f}", ">= 2.0 required",
                "PASS" if fos_p >= 2.0 else "FAIL")
    else:
        _p(doc, "No differential head — piping not a concern.")
        _result(doc, "Piping FOS", "N/A", "No differential head", "PASS")

    doc.add_heading(f'{ch}.2 Uplift Pressure at Base', level=2)
    u_base_behind = max(0, gamma_w * (exc_depth - gwt_behind))
    u_base_front = max(0, gamma_w * (exc_depth - gwt_front))
    _f(doc, f"u at base (behind) = {gamma_w} x ({exc_depth:.1f} - {gwt_behind:.1f}) = {u_base_behind:.1f} kPa")
    _f(doc, f"u at base (front) = {gamma_w} x ({exc_depth:.1f} - {gwt_front:.1f}) = {u_base_front:.1f} kPa")
    _f(doc, f"Net uplift = {u_base_behind:.1f} - {u_base_front:.1f} = {u_base_behind - u_base_front:.1f} kPa")
    _p(doc, "Dewatering below excavation base eliminates uplift pressure on the front side.")


