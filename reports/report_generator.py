# 10. WIND LOAD (IS 875-3)
# ═══════════════════════════════════════════════════════════════
def _sec_wind(doc, ch, result_wind, result_earth, Vb, barrier_h, exc_depth):
    doc.add_heading(f'{ch}. Wind Barrier Load — IS 875 (Part 3):2015', level=1)
    _ref(doc, "Ref: IS 875 (Part 3):2015 Cl. 6.3, 7.2, 7.4.2.2")

    doc.add_heading(f'{ch}.1 Design Wind Speed', level=2)
    _f(doc, "V_z = V_b x k_1 x k_2 x k_3")
    _p(doc, "Where:")
    _f(doc, f"V_b = {Vb:.0f} m/s  (basic wind speed, IS 875 Fig. 1)")
    _f(doc, "k_1 = 1.0  (risk coefficient, Table 1 — temporary structure)")
    _f(doc, "k_2 = 0.97  (terrain Cat 3, height ~10m, Table 2)")
    _f(doc, "k_3 = 1.0  (topography factor)")
    Vz = Vb * 1.0 * 0.97 * 1.0
    _f(doc, f"V_z = {Vb:.0f} x 1.0 x 0.97 x 1.0 = {Vz:.1f} m/s")

    doc.add_heading(f'{ch}.2 Design Wind Pressure', level=2)
    _f(doc, "p_z = 0.6 x V_z^2  (Cl. 6.3)")
    pz = 0.6 * Vz**2
    _f(doc, f"p_z = 0.6 x {Vz:.1f}^2 = {pz:.1f} N/m2 = {pz/1000:.3f} kPa")

    doc.add_heading(f'{ch}.3 Wind Force on Hoarding', level=2)
    _ref(doc, "Ref: IS 875 Cl. 7.4.2.2 — Cf for hoardings = 1.2 (above ground)")
    _f(doc, f"F_wind = Cf x p_z x H_barrier")
    Cf = 1.2
    Fw = Cf * pz / 1000 * barrier_h
    Mw = Fw * barrier_h / 2
    _f(doc, f"F_wind = {Cf} x {pz/1000:.3f} x {barrier_h:.0f} = {Fw:.2f} kN/m")
    _f(doc, f"M_wind = F_wind x H/2 = {Fw:.2f} x {barrier_h/2:.1f} = {Mw:.2f} kN.m/m")
    _p(doc, "Applied as point load and moment at wall top in the beam FE model.")

    doc.add_heading(f'{ch}.4 Combined Results', level=2)
    if result_wind is not None and result_earth is not None:
        inc_bm = (result_wind.max_moment / result_earth.max_moment - 1) * 100 if result_earth.max_moment > 0 else 0
        inc_sf = (result_wind.max_shear / result_earth.max_shear - 1) * 100 if result_earth.max_shear > 0 else 0
        _table(doc,
            ["Parameter", "Earth Only", "Earth + Wind", "Increase"],
            [
                ["Max BM (kN.m/m)", f"{result_earth.max_moment:.1f}", f"{result_wind.max_moment:.1f}", f"+{inc_bm:.0f}%"],
                ["Max SF (kN/m)", f"{result_earth.max_shear:.1f}", f"{result_wind.max_shear:.1f}", f"+{inc_sf:.0f}%"],
            ], [3, 3, 3, 3])
        _p(doc, f"Wind increases design BM by {inc_bm:.0f}%. "
           f"{'Governs design.' if inc_bm > 0 else ''}", bold=True)


