# WATER PRESSURE DETAILED
# ═══════════════════════════════════════════════════════════════
def _sec_water_pressure(doc, ch, gwt_behind, gwt_front, exc_depth, wall_toe):
    doc.add_heading(f'{ch}. Water Pressure — Detailed Calculation', level=1)
    _ref(doc, "Ref: IS 14458 Cl. 5.1.3 — Hydrostatic and seepage pressure")

    gamma_w = 9.81
    doc.add_heading(f'{ch}.1 Hydrostatic Water Pressure Behind Wall', level=2)
    _p(doc, "Water pressure acts behind the wall below the water table. "
       "Assuming hydrostatic (no seepage reduction):")
    _f(doc, "u_behind(z) = gamma_w x (z - GWT_behind)   for z > GWT_behind")
    _f(doc, f"GWT behind = {gwt_behind:.1f} m below GL")

    rows = []
    for z in [gwt_behind, exc_depth, wall_toe]:
        u = max(0, gamma_w * (z - gwt_behind))
        rows.append([f"{z:.1f}", f"{max(0, z - gwt_behind):.1f}", f"{u:.1f}"])
    _table(doc,
        ["Depth (m)", "Head (m)", "u (kPa)"],
        rows, [4, 4, 4])

    _p(doc, "Total water thrust behind wall (triangular distribution):")
    h_w = wall_toe - gwt_behind
    if h_w > 0:
        u_max = gamma_w * h_w
        Fw = 0.5 * u_max * h_w
        _f(doc, f"h_w = {wall_toe:.1f} - {gwt_behind:.1f} = {h_w:.1f} m")
        _f(doc, f"u_max = {gamma_w} x {h_w:.1f} = {u_max:.1f} kPa")
        _f(doc, f"F_water = 0.5 x {u_max:.1f} x {h_w:.1f} = {Fw:.1f} kN/m")

    doc.add_heading(f'{ch}.2 Water Pressure In Front of Wall', level=2)
    _f(doc, "u_front(z) = gamma_w x (z - GWT_front)   for z > GWT_front")
    _f(doc, f"GWT front = {gwt_front:.1f} m below GL (after dewatering)")

    if gwt_front < wall_toe:
        h_f = wall_toe - gwt_front
        u_f = gamma_w * h_f
        Ff = 0.5 * u_f * h_f
        _f(doc, f"F_water_front = 0.5 x {u_f:.1f} x {h_f:.1f} = {Ff:.1f} kN/m")
    else:
        _p(doc, "GWT in front is below wall toe — no front water pressure.")

    doc.add_heading(f'{ch}.3 Net Water Pressure', level=2)
    _p(doc, "The differential water pressure drives the wall toward the excavation. "
       "This is additional to the effective earth pressure.")
    _f(doc, "Delta_u(z) = u_behind(z) - u_front(z)")
    _p(doc, "This net water pressure is included in the beam FE loading model.")


