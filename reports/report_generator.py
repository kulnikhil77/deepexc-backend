# BASE HEAVE STABILITY
# ═══════════════════════════════════════════════════════════════
def _sec_base_heave(doc, ch, layers, exc_depth, surcharge, wall_toe, n_anchors):
    doc.add_heading(f'{ch}. Base Heave Stability Check', level=1)
    _ref(doc, "Ref: FHWA-IF-99-015 Section 5.7; Terzaghi (1943)")

    doc.add_heading(f'{ch}.1 Terzaghi Method', level=2)
    _p(doc, "Base heave occurs when the weight of soil outside the excavation exceeds "
       "the bearing capacity of the soil below the excavation base.")
    _f(doc, "FOS_heave = (c_u x N_c + q_s) / (gamma x H + q)")
    _p(doc, "Where:")
    _f(doc, "c_u  = undrained shear strength at base level")
    _f(doc, "N_c  = bearing capacity factor (5.14 for strip footing)")
    _f(doc, "q_s  = surcharge on passive side (usually 0)")
    _f(doc, "gamma = weighted average unit weight above base")
    _f(doc, "H    = excavation depth")
    _f(doc, "q    = surcharge at GL")

    # Find soil at base
    cu_base = 0; gamma_avg = 0; total_h = 0
    for L in layers:
        top = L.get('top', 0); bot = L.get('bottom', 99)
        if top >= exc_depth: break
        h_in = min(exc_depth, bot) - top
        gamma_avg += L.get('gamma', 18) * h_in
        total_h += h_in
        if bot >= exc_depth:
            cu_base = L.get('c', 0)
    gamma_avg = gamma_avg / total_h if total_h > 0 else 18.0

    _p(doc, "Calculation:", bold=True)
    _f(doc, f"c_u at base = {cu_base:.0f} kPa")
    _f(doc, f"N_c = 5.14  (strip footing, Terzaghi)")
    _f(doc, f"gamma_avg = {gamma_avg:.1f} kN/m3  (weighted average)")
    _f(doc, f"H = {exc_depth:.1f} m")
    _f(doc, f"q = {surcharge:.1f} kPa")

    driving = gamma_avg * exc_depth + surcharge
    resisting = cu_base * 5.14
    fos = resisting / driving if driving > 0 else 999
    _f(doc, f"Driving = {gamma_avg:.1f} x {exc_depth:.1f} + {surcharge:.1f} = {driving:.1f} kPa")
    _f(doc, f"Resisting = {cu_base:.0f} x 5.14 = {resisting:.1f} kPa")
    _f(doc, f"FOS = {resisting:.1f} / {driving:.1f} = {fos:.2f}")

    if fos >= 2.0:
        _result(doc, "Base heave FOS", f"{fos:.2f}", ">= 2.0 required", "PASS")
    elif fos >= 1.5:
        _result(doc, "Base heave FOS", f"{fos:.2f}", ">= 2.0 preferred", "MARGINAL")
    else:
        _result(doc, "Base heave FOS", f"{fos:.2f}", ">= 2.0 required", "FAIL")

    if cu_base < 10:
        _p(doc, "Note: c_u at base is low or zero (granular soil). For granular soils, "
           "base heave is not typically critical; the check is more relevant for cohesive soils. "
           "In granular soils, hydraulic uplift (piping) governs — see next section.")

    doc.add_heading(f'{ch}.2 FHWA Exemption Check', level=2)
    _ref(doc, "Ref: FHWA-IF-99-015 Section 5.7 — Base stability exemption criteria")
    embed = wall_toe - exc_depth
    _p(doc, f"Wall embedment below excavation: {embed:.1f} m")
    _p(doc, f"Number of anchor levels: {n_anchors}")
    if n_anchors >= 2 and embed >= 0.5 * exc_depth:
        _p(doc, "With >= 2 anchor levels and adequate embedment, "
           "FHWA allows exemption from detailed base heave analysis for most soil conditions.")
        _result(doc, "FHWA exemption", "Applicable", "", "PASS")
    else:
        _p(doc, "FHWA exemption criteria not fully met. Detailed check required as above.")


