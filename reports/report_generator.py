# 2. GEOTECHNICAL PROFILE & EARTH PRESSURE
# ═══════════════════════════════════════════════════════════════
def _sec_soil(doc, ch, layers, gwt_behind, gwt_front, surcharge, exc_depth):
    doc.add_heading(f'{ch}. Geotechnical Profile & Earth Pressure Coefficients', level=1)

    doc.add_heading(f'{ch}.1 Soil Stratification', level=2)
    _ref(doc, "Ref: IS 9527 Cl. 4.2 — Earth pressure from design soil parameters")
    _p(doc, "The soil profile adopted for design is based on the geotechnical investigation report. "
       "The following table summarises the design parameters for each stratum:")

    rows = []
    for i, L in enumerate(layers):
        phi = L.get('phi', 30); c = L.get('c', 0)
        Ka = math.tan(math.radians(45 - phi/2))**2
        Kp = math.tan(math.radians(45 + phi/2))**2
        rows.append([
            L.get('name', f'Layer {i+1}'),
            f"{L.get('top',0):.1f}", f"{L.get('bottom',0):.1f}",
            f"{L.get('bottom',0) - L.get('top',0):.1f}",
            f"{L.get('gamma',18):.1f}", f"{phi:.0f}", f"{c:.0f}",
            f"{Ka:.4f}", f"{Kp:.4f}",
        ])
    _table(doc,
        ["Stratum", "Top (m)", "Bot (m)", "Thick (m)", "gamma (kN/m3)", "phi (deg)", "c' (kPa)", "Ka", "Kp"],
        rows, [2, 1.3, 1.3, 1.3, 1.8, 1.3, 1.3, 1.5, 1.5], font_size=8)

    doc.add_heading(f'{ch}.2 Groundwater Conditions', level=2)
    _table(doc,
        ["Parameter", "Value", "Remarks"],
        [
            ["GWT behind wall", f"{gwt_behind:.1f} m below GL", "Design water table (monsoon / permanent)"],
            ["GWT in front of wall", f"{gwt_front:.1f} m below GL", "Post-dewatering level"],
            ["gamma_w", "9.81 kN/m3", "Unit weight of water"],
            ["Differential head", f"{gwt_front - gwt_behind:.1f} m", "Drives net water pressure"],
        ], [4, 4, 6])

    doc.add_heading(f'{ch}.3 Earth Pressure Coefficient — Derivation', level=2)
    _ref(doc, "Ref: IS 9527 Cl. 4.2 — Rankine theory for smooth walls")
    _p(doc, "For a smooth vertical wall retaining horizontal backfill, the Rankine earth pressure "
       "coefficients are:")
    _f(doc, "Ka = tan2(45 - phi/2)     ... Active coefficient")
    _f(doc, "Kp = tan2(45 + phi/2)     ... Passive coefficient")
    _p(doc, "Derivation per stratum:")

    for L in layers:
        phi = L.get('phi', 30)
        Ka = math.tan(math.radians(45 - phi/2))**2
        Kp = math.tan(math.radians(45 + phi/2))**2
        _p(doc, f"{L.get('name', 'Layer')} (phi = {phi:.0f} deg):", bold=True)
        _f(doc, f"Ka = tan2(45 - {phi:.0f}/2) = tan2({45 - phi/2:.1f}) = {Ka:.4f}")
        _f(doc, f"Kp = tan2(45 + {phi:.0f}/2) = tan2({45 + phi/2:.1f}) = {Kp:.4f}")
        _f(doc, f"sqrt(Ka) = {math.sqrt(Ka):.4f}  (for cohesion term)")
        _f(doc, f"sqrt(Kp) = {math.sqrt(Kp):.4f}")

    doc.add_heading(f'{ch}.4 Active Earth Pressure at Key Depths', level=2)
    _ref(doc, "Ref: IS 9527 Cl. 4.2; IS 14458 Cl. 5.1.2 — Effective stress approach")
    _p(doc, "Effective horizontal active stress at depth z:")
    _f(doc, "sigma_ah'(z) = Ka x sigma_v'(z) - 2c' x sqrt(Ka)  >= 0")
    _p(doc, "Effective vertical stress:")
    _f(doc, "sigma_v'(z) = Sum(gamma_i x h_i) + q - u(z)")
    _p(doc, "Hydrostatic pore water pressure:")
    _f(doc, "u(z) = gamma_w x (z - z_wt)    for z > z_wt,  else 0")
    _p(doc, "Total active pressure (effective + water):")
    _f(doc, "p_a(z) = sigma_ah'(z) + u(z)")

    _p(doc, "Detailed computation at layer boundaries and key depths:", bold=True)

    gamma_w = 9.81
    sigma_v = surcharge  # effective vertical stress at GL
    cum = 0.0
    press_rows = [["0.00 (GL)", f"{surcharge:.2f}", "0.00", "-", "-", "0.00", "Surcharge q only"]]

    for L in layers:
        top = L.get('top', cum); bot = L.get('bottom', cum + 3)
        gamma = L.get('gamma', 18.0); phi = L.get('phi', 30); c = L.get('c', 0)
        Ka = math.tan(math.radians(45 - phi/2))**2
        thick = bot - top

        # Just below top of layer (if not at GL)
        if top > 0.01:
            _p(doc, f"At z = {top:.2f} m (top of {L.get('name','')}):")
            u_top = max(0, gamma_w * (top - gwt_behind))
            sig_ah_top = max(0, Ka * sigma_v - 2 * c * math.sqrt(Ka))
            _f(doc, f"sigma_v' = {sigma_v:.2f} kPa")
            _f(doc, f"sigma_ah' = {Ka:.4f} x {sigma_v:.2f} - 2 x {c:.0f} x {math.sqrt(Ka):.4f} = {sig_ah_top:.2f} kPa")
            _f(doc, f"u = {u_top:.2f} kPa")
            _f(doc, f"p_a = {sig_ah_top:.2f} + {u_top:.2f} = {sig_ah_top + u_top:.2f} kPa")
            press_rows.append([f"{top:.2f}", f"{sigma_v:.2f}", f"{sig_ah_top:.2f}",
                              f"{Ka:.4f}", f"{c:.0f}", f"{u_top:.2f}",
                              f"Top of {L.get('name','')}"])

        # Bottom of layer
        if bot > gwt_behind:
            above = max(0, gwt_behind - top)
            below = thick - above
            sigma_v += gamma * above + (gamma - gamma_w) * below
            u_bot = gamma_w * (bot - gwt_behind)
        else:
            sigma_v += gamma * thick
            u_bot = 0

        sig_ah_bot = max(0, Ka * sigma_v - 2 * c * math.sqrt(Ka))
        pa_bot = sig_ah_bot + u_bot

        _p(doc, f"At z = {bot:.2f} m (bottom of {L.get('name','')}):")
        _f(doc, f"sigma_v' = {sigma_v:.2f} kPa")
        _f(doc, f"sigma_ah' = {Ka:.4f} x {sigma_v:.2f} - 2 x {c:.0f} x {math.sqrt(Ka):.4f} = {sig_ah_bot:.2f} kPa")
        _f(doc, f"u = {u_bot:.2f} kPa")
        _f(doc, f"p_a = {sig_ah_bot:.2f} + {u_bot:.2f} = {pa_bot:.2f} kPa")

        press_rows.append([f"{bot:.2f}", f"{sigma_v:.2f}", f"{sig_ah_bot:.2f}",
                          f"{Ka:.4f}", f"{c:.0f}", f"{u_bot:.2f}",
                          f"Bot of {L.get('name','')}"])
        cum = bot

    # Pressure at excavation level
    _p(doc, f"At excavation level z = {exc_depth:.2f} m:", bold=True)
    _f(doc, f"Active pressure at excavation level is the critical depth for wall design.")

    _p(doc, "Summary — Active Pressure Profile:", bold=True)
    _table(doc,
        ["Depth (m)", "sigma_v' (kPa)", "sigma_ah' (kPa)", "Ka", "c' (kPa)", "u (kPa)", "Notes"],
        press_rows, [1.8, 2, 2, 1.5, 1.3, 1.5, 3.5], font_size=8)

    doc.add_heading(f'{ch}.5 Passive Earth Pressure Below Excavation', level=2)
    _ref(doc, "Ref: IS 9527 Cl. 4.2 — Passive resistance; factored by FOS")
    _p(doc, "Below excavation level, passive resistance develops in front of the wall:")
    _f(doc, "sigma_ph'(z) = Kp x sigma_v'(z) + 2c' x sqrt(Kp)")
    _p(doc, "The passive pressure is factored by FOS_passive (typically 1.5) for design:")
    _f(doc, "sigma_ph_design = sigma_ph / FOS_passive")
    _p(doc, "Net pressure below excavation:")
    _f(doc, "p_net(z) = max(0, p_active(z) - p_passive_design(z))")
    _p(doc, "The net pressure is clipped to zero — passive resistance can reduce "
       "active loading but cannot push the wall backward (physically, passive is a reaction).")


