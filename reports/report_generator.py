# 8. ANCHOR DESIGN — DETAILED
# ═══════════════════════════════════════════════════════════════
def _sec_anchor_design(doc, ch, anchors_ui, anchor_designs, anchor_reactions, exc_depth):
    doc.add_heading(f'{ch}. Anchor Design — Detailed Calculations', level=1)
    _ref(doc, "Ref: IS 9527 Cl. 4.2; FHWA-IF-99-015 Ch. 4; BS 8081:2015 Cl. 6.6")

    _p(doc, "Design procedure for each anchor tier:", bold=True)
    _p(doc, "Step 1: Compute design force from wall analysis reaction.")
    _f(doc, "F_design = R_horizontal x spacing / cos(alpha)")
    _p(doc, "Step 2: Select tendon and check tensile capacity.")
    _f(doc, "F_capacity >= F_design x FOS_tendon")
    _p(doc, "Step 3: Compute required bond length.")
    _f(doc, "L_bond = F_design x FOS_bond / (pi x d_hole x tau_bond)")
    _p(doc, "Step 4: Determine free length (FHWA minimum 3.0 m or beyond failure plane).")
    _f(doc, "L_free >= max(0.15H + d_anchor_level, 3.0 m)")
    _p(doc, "Step 5: Total anchor length = free length + bond length.")

    for i, ad in enumerate(anchor_designs):
        anc = anchors_ui[i]
        level = anc['level'] if isinstance(anc, dict) else anc.level
        incl = anc.get('incl', 20) if isinstance(anc, dict) else getattr(anc, 'inclination', 20)
        spc = anc.get('spacing', 3.0) if isinstance(anc, dict) else getattr(anc, 'horizontal_spacing', 3.0)
        tau = anc.get('bond_stress', 200) if isinstance(anc, dict) else getattr(anc, 'bond_stress', 200)
        d_hole = anc.get('drill_dia', 115) if isinstance(anc, dict) else getattr(anc, 'drill_diameter', 0.115)
        if d_hole > 1: d_hole = d_hole / 1000
        a_type = anc.get('type', 'rebar') if isinstance(anc, dict) else getattr(anc, 'anchor_type', 'rebar')

        doc.add_heading(f'{ch}.{i+1} Anchor {i+1} at {level:.1f} m — {a_type.upper()}', level=2)

        r_horiz = abs(anchor_reactions[i])
        r_per = r_horiz * spc
        bar_f = r_per / math.cos(math.radians(incl))

        _p(doc, "Step 1 — Design anchor force:", bold=True)
        _f(doc, f"R_horizontal = {r_horiz:.2f} kN/m  (from beam FE)")
        _f(doc, f"Spacing = {spc:.1f} m c/c")
        _f(doc, f"Force per anchor = {r_horiz:.2f} x {spc:.1f} = {r_per:.2f} kN")
        _f(doc, f"Inclination alpha = {incl:.0f} deg")
        _f(doc, f"Force along anchor axis = {r_per:.2f} / cos({incl:.0f}) = {bar_f:.2f} kN")

        _p(doc, "Step 2 — Tendon capacity:", bold=True)
        _ref(doc, f"Type: {a_type.upper()}")
        _f(doc, f"Tendon capacity = {ad.tendon_force:.1f} kN")
        _f(doc, f"FOS_tendon = {ad.tendon_force:.1f} / {bar_f:.1f} = {ad.fos_tendon_actual:.2f}")
        status_t = "PASS" if ad.fos_tendon_actual >= 1.15 else "FAIL"
        _result(doc, "Tendon check", f"FOS = {ad.fos_tendon_actual:.2f}", f"(>= 1.15 req'd)", status_t)

        _p(doc, "Step 3 — Bond length:", bold=True)
        _ref(doc, f"Ref: IS 456 Cl. 26.2.1; d_hole = {d_hole*1000:.0f} mm, tau = {tau:.0f} kPa")
        bond_cap = math.pi * d_hole * tau
        fos_b = 2.0
        Lb = bar_f * fos_b / bond_cap
        _f(doc, f"Bond capacity per m = pi x {d_hole*1000:.0f}/1000 x {tau:.0f} = {bond_cap:.2f} kN/m")
        _f(doc, f"L_bond = {bar_f:.2f} x {fos_b:.1f} / {bond_cap:.2f} = {Lb:.2f} m")
        _result(doc, "Bond FOS", f"{ad.fos_bond_actual:.2f}", f"(>= 2.0 req'd)",
                "PASS" if ad.fos_bond_actual >= 2.0 else "FAIL")

        _p(doc, "Step 4 & 5 — Free length and total:", bold=True)
        _f(doc, f"Free length (minimum) = max(0.15 x {exc_depth:.0f} + {level:.1f}, 3.0) = {max(0.15*exc_depth+level, 3.0):.1f} m")
        _f(doc, f"Total anchor length = {ad.total_length:.1f} m")

        _p(doc, "Summary:", bold=True)
        _table(doc,
            ["Check", "Required", "Provided", "FOS", "Status"],
            [
                ["Tendon", f"{bar_f:.1f} kN", f"{ad.tendon_force:.1f} kN", f"{ad.fos_tendon_actual:.2f}", "OK" if ad.fos_tendon_actual >= 1.15 else "CHECK"],
                ["Bond", f"{Lb:.2f} m", f"{ad.bond_length_required:.2f} m", f"{ad.fos_bond_actual:.2f}", "OK" if ad.fos_bond_actual >= 2.0 else "CHECK"],
                ["Total length", "-", f"{ad.total_length:.1f} m", "-", "-"],
                ["Recommendation", "-", ad.tendon_recommendation, "-", ad.status],
            ], [2.5, 2.5, 3, 2, 2.5])


