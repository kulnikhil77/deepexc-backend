# 9. SECTION CAPACITY CHECK — IS 800:2007
# ═══════════════════════════════════════════════════════════════
def _sec_capacity(doc, ch, section, grade, design_bm, design_sf, EI):
    doc.add_heading(f'{ch}. Section Adequacy — IS 800:2007', level=1)
    if not section:
        _p(doc, "Section object not available for capacity check.")
        return

    fy = 355 if '355' in grade else (240 if '240' in grade else 355)
    gm0 = 1.10

    doc.add_heading(f'{ch}.1 Bending Capacity (Cl. 8.2.1.2)', level=2)
    _ref(doc, "Ref: IS 800:2007 Cl. 8.2.1.2 — Design bending strength")
    _p(doc, "For a laterally supported beam (sheet pile wall embedded in soil):")
    _f(doc, "M_d = beta_b x Z_p x f_y / gamma_m0")
    _p(doc, "where beta_b = 1.0 for Class 1 and 2 sections (plastic/compact).")
    _f(doc, f"M_d = 1.0 x {section.plastic_modulus:.0f} x {fy} / {gm0}")
    _f(doc, f"M_d = {section.plastic_modulus:.0f} x {fy} / ({gm0} x 10^3)  [cm3 to m, MPa to kN]")
    Md = fy * section.plastic_modulus * 1e-3 / gm0
    _f(doc, f"M_d = {Md:.2f} kN.m/m")
    _p(doc, "")
    _f(doc, f"Design BM = {abs(design_bm):.2f} kN.m/m")
    util_bm = abs(design_bm) / Md if Md > 0 else 999
    _f(doc, f"Utilization = {abs(design_bm):.2f} / {Md:.2f} = {util_bm:.4f} ({util_bm*100:.1f}%)")
    _result(doc, "BENDING CHECK", f"{util_bm*100:.1f}%", "utilization", "PASS" if util_bm <= 1.0 else "FAIL")

    # Also check elastic moment capacity
    _p(doc, "Elastic moment capacity (for comparison):")
    Me = fy * section.elastic_modulus * 1e-3 / gm0
    _f(doc, f"M_e = f_y x Z_e / gamma_m0 = {fy} x {section.elastic_modulus:.0f} / ({gm0} x 10^3) = {Me:.2f} kN.m/m")

    doc.add_heading(f'{ch}.2 Shear Capacity (Cl. 8.4.1)', level=2)
    _ref(doc, "Ref: IS 800:2007 Cl. 8.4.1 — Design shear strength")
    _f(doc, "V_d = A_v x f_yw / (sqrt(3) x gamma_m0)")
    _p(doc, "For Z-profile and U-profile sheet piles, the shear area is approximately:")
    _f(doc, "A_v = h x t_w  (per m of wall)")
    Av_mm2 = section.height * section.thickness_web  # mm2 per pile, approx
    Av_cm2 = Av_mm2 / 100  # to cm2
    _f(doc, f"A_v = {section.height:.0f} x {section.thickness_web:.1f} = {Av_mm2:.0f} mm2")
    Vd = fy * Av_mm2 / (math.sqrt(3) * gm0 * 1000)  # kN
    _f(doc, f"V_d = {Av_mm2:.0f} x {fy} / (1.732 x {gm0} x 1000)")
    _f(doc, f"V_d = {Vd:.2f} kN/m")
    util_sf = abs(design_sf) / Vd if Vd > 0 else 999
    _f(doc, f"Design SF = {abs(design_sf):.2f} kN/m")
    _f(doc, f"Utilization = {abs(design_sf):.2f} / {Vd:.2f} = {util_sf:.4f} ({util_sf*100:.1f}%)")
    _result(doc, "SHEAR CHECK", f"{util_sf*100:.1f}%", "utilization", "PASS" if util_sf <= 1.0 else "FAIL")

    doc.add_heading(f'{ch}.3 Moment-Shear Interaction (Cl. 9.2.2)', level=2)
    _ref(doc, "Ref: IS 800:2007 Cl. 9.2.2 — If V <= 0.6 Vd, no reduction in moment capacity")
    v_ratio = abs(design_sf) / Vd if Vd > 0 else 0
    util_int = 0
    if v_ratio <= 0.6:
        _p(doc, f"V/Vd = {v_ratio:.3f} <= 0.6  =>  No reduction in moment capacity required.")
        _result(doc, "Interaction check", "Not required", "(V/Vd <= 0.6)", "PASS")
    else:
        beta_mv = (2 * v_ratio - 1)**2
        Md_red = Md * (1 - beta_mv)
        _f(doc, f"V/Vd = {v_ratio:.3f} > 0.6  =>  Reduced moment capacity:")
        _f(doc, f"beta = (2V/Vd - 1)^2 = {beta_mv:.4f}")
        _f(doc, f"Md_reduced = {Md:.2f} x (1 - {beta_mv:.4f}) = {Md_red:.2f} kN.m/m")
        util_int = abs(design_bm) / Md_red if Md_red > 0 else 999
        _f(doc, f"Utilization = {abs(design_bm):.2f} / {Md_red:.2f} = {util_int:.4f} ({util_int*100:.1f}%)")
        _result(doc, "Interaction check", f"{util_int*100:.1f}%", "utilization",
                "PASS" if util_int <= 1.0 else "FAIL")

    doc.add_heading(f'{ch}.4 Overall Section Adequacy', level=2)
    overall = max(util_bm, util_sf, util_int)
    _p(doc, "Summary of section checks:", bold=True)
    int_status = "PASS" if util_int <= 1.0 else "FAIL"
    int_label = f"{util_int*100:.1f}%" if util_int > 0 else "N/A"
    _table(doc,
        ["Check", "Demand", "Capacity", "Utilization", "Status"],
        [
            ["Bending (Cl. 8.2.1)", f"{abs(design_bm):.1f} kN.m/m", f"{Md:.1f} kN.m/m",
             f"{util_bm*100:.1f}%", "PASS" if util_bm <= 1.0 else "FAIL"],
            ["Shear (Cl. 8.4.1)", f"{abs(design_sf):.1f} kN/m", f"{Vd:.1f} kN/m",
             f"{util_sf*100:.1f}%", "PASS" if util_sf <= 1.0 else "FAIL"],
            ["M-V Interaction (Cl. 9.2.2)", "-", "-", int_label, int_status],
        ], [3.5, 3, 3, 2, 2])
    _result(doc, "OVERALL", f"{overall*100:.1f}%", "max utilization",
            "PASS" if overall <= 1.0 else "FAIL")

    # ── Remedial measures if any check fails ──
    if overall > 1.0:
        doc.add_heading(f'{ch}.5 Remedial Measures', level=2)
        _p(doc, "The selected section is INADEQUATE for the design forces. "
           "The following remedial measures are recommended:", bold=True, color=RED)

        if util_bm > 1.0:
            _p(doc, f"1. Bending utilization is {util_bm*100:.0f}%. Options:")
            _f(doc, "a) Select a heavier section with higher Z_p (plastic modulus)")
            _f(doc, f"   Required Z_p >= {abs(design_bm) * gm0 * 1e3 / fy:.0f} cm3/m")
            _f(doc, "b) Upgrade steel grade (e.g. S430GP -> higher f_y)")
            _f(doc, "c) Add an additional anchor level to reduce span BM")
            _f(doc, "d) Reduce anchor spacing to increase support stiffness")

        if util_sf > 1.0:
            _p(doc, f"{'2' if util_bm > 1.0 else '1'}. Shear utilization is {util_sf*100:.0f}%. Options:")
            _f(doc, "a) Select a section with thicker web (higher A_v)")
            _f(doc, f"   Required A_v >= {abs(design_sf) * math.sqrt(3) * gm0 * 1000 / fy:.0f} mm2")
            _f(doc, "b) Add web stiffeners at high-shear zones (anchor levels)")

        if util_int > 1.0:
            n = sum(1 for u in [util_bm, util_sf] if u > 1.0) + 1
            _p(doc, f"{n}. Moment-shear interaction fails ({util_int*100:.0f}%). "
               "This occurs because high shear reduces the available bending capacity. Options:")
            _f(doc, "a) Reduce the shear demand by adding intermediate supports")
            _f(doc, "b) Select a section where V/Vd < 0.6 to eliminate interaction")
            _f(doc, f"   Required Vd > {abs(design_sf)/0.6:.0f} kN/m to avoid interaction")

        _p(doc, "")
        _p(doc, "ACTION REQUIRED: Revise the wall section or support arrangement and re-run "
           "the analysis until all checks pass. Do not proceed with construction "
           "using the current section.", bold=True, color=RED)


