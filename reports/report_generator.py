def _cover(doc, project_name, location, exc_depth, mode_title,
           firm="", engineer="", revision="R0"):
    for _ in range(5): doc.add_paragraph()
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(mode_title.upper())
    r.font.size = Pt(28); r.bold = True; r.font.color.rgb = NAVY; r.font.name = FONT

    doc.add_paragraph()
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run("DETAILED CALCULATION REPORT")
    r.font.size = Pt(16); r.font.color.rgb = ACCENT; r.font.name = FONT

    doc.add_paragraph(); doc.add_paragraph()

    lines = [
        (project_name, 16, True), (location, 13, False),
        (f"Excavation Depth: {exc_depth:.1f} m", 12, False), ("", 10, False),
    ]
    if firm: lines.append((firm, 13, True))
    if engineer: lines.append((f"Prepared by: {engineer}", 11, False))
    lines.append((f"Date: {date.today().strftime('%d %B %Y')}", 11, False))
    lines.append((f"Revision: {revision}", 11, False))

    for text, size, bold in lines:
        p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(text); r.font.size = Pt(size); r.bold = bold
        r.font.name = FONT; r.font.color.rgb = DARK


