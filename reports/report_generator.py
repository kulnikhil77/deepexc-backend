# SURCHARGE EFFECT
# ═══════════════════════════════════════════════════════════════
def _sec_surcharge(doc, ch, surcharge, layers, exc_depth):
    doc.add_heading(f'{ch}. Surcharge Loading Effect', level=1)
    _ref(doc, "Ref: IS 9527 Cl. 4.2.3 — Surcharge on active side")

    doc.add_heading(f'{ch}.1 Uniform Surcharge', level=2)
    _p(doc, "A uniform surcharge q at ground level increases the vertical effective stress "
       "at all depths, thereby increasing active earth pressure on the wall.")
    _f(doc, "Additional active pressure due to surcharge:")
    _f(doc, "Delta_sigma_ah = Ka x q")
    _p(doc, "This is constant with depth (uniform surcharge).")

    _p(doc, "Effect per layer:", bold=True)
    for L in layers:
        phi = L.get('phi', 30)
        Ka = math.tan(math.radians(45 - phi/2))**2
        delta_p = Ka * surcharge
        _f(doc, f"{L.get('name', 'Layer')}: Ka = {Ka:.4f}, Delta_p = {Ka:.4f} x {surcharge:.1f} = {delta_p:.2f} kPa")

    doc.add_heading(f'{ch}.2 Total Additional Force Due to Surcharge', level=2)
    _p(doc, "The total additional horizontal force due to surcharge on the wall:")
    for L in layers:
        phi = L.get('phi', 30)
        Ka = math.tan(math.radians(45 - phi/2))**2
        top = L.get('top', 0); bot = min(L.get('bottom', 99), exc_depth + 5)
        h = bot - top
        F_add = Ka * surcharge * h
        _f(doc, f"{L.get('name', '')}: F = {Ka:.4f} x {surcharge:.1f} x {h:.1f} = {F_add:.2f} kN/m")

    _p(doc, "The surcharge is already included in the beam FE loading model as part of "
       "the total active pressure computation at each node.")


