NAVY = RGBColor(0x1A, 0x35, 0x50)
DARK = RGBColor(0x2C, 0x3E, 0x50)
GRAY = RGBColor(0x7F, 0x8C, 0x8D)
WHITE = RGBColor(0xFF, 0xFF, 0xFF)
ACCENT = RGBColor(0x2E, 0x86, 0xC1)
RED = RGBColor(0xC0, 0x39, 0x2B)
GREEN = RGBColor(0x27, 0xAE, 0x60)
FONT = 'Arial'


def _shading(cell, hex_color):
    el = cell._element.get_or_add_tcPr()
    el.append(el.makeelement(qn('w:shd'), {qn('w:fill'): hex_color, qn('w:val'): 'clear'}))


def _table(doc, headers, rows, col_widths=None, font_size=9):
    t = doc.add_table(rows=1 + len(rows), cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = 'Table Grid'
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]
        c.text = h
        p = c.paragraphs[0]; p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.runs[0]; r.bold = True; r.font.size = Pt(font_size); r.font.color.rgb = WHITE; r.font.name = FONT
        _shading(c, '1A3550')
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            c = t.rows[ri + 1].cells[ci]
            c.text = str(val)
            p = c.paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER if ci > 0 else WD_ALIGN_PARAGRAPH.LEFT
            for r in p.runs: r.font.size = Pt(font_size); r.font.name = FONT
            if ri % 2 == 1: _shading(c, 'EBF5FB')
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in t.rows: row.cells[i].width = Cm(w)
    return t


def _f(doc, text):
    """Formula line — indented, monospace."""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(1.2)
    p.paragraph_format.space_before = Pt(2)
    p.paragraph_format.space_after = Pt(2)
    r = p.add_run(text)
    r.font.name = 'Consolas'; r.font.size = Pt(9.5); r.font.color.rgb = NAVY


def _p(doc, text, bold=False, italic=False, size=10, color=DARK, after=6):
    """Paragraph."""
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(after)
    r = p.add_run(text)
    r.font.name = FONT; r.font.size = Pt(size); r.font.color.rgb = color
    r.bold = bold; r.italic = italic
    return p


def _ref(doc, text):
    """IS code clause reference."""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(1.2)
    p.paragraph_format.space_after = Pt(2)
    r = p.add_run(text)
    r.font.name = FONT; r.font.size = Pt(8.5); r.font.color.rgb = ACCENT; r.italic = True


def _result(doc, label, value, unit, status=None):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(3)
    r = p.add_run(f"  {label}: "); r.font.name = FONT; r.font.size = Pt(10); r.bold = True
    r = p.add_run(f"{value} {unit}"); r.font.name = FONT; r.font.size = Pt(10)
    if status:
        r = p.add_run(f"  [{status}]"); r.font.name = FONT; r.font.size = Pt(10); r.bold = True
        r.font.color.rgb = GREEN if status == "PASS" else RED


def _fig(doc, fig, caption, width=6.0):
    buf = io.BytesIO()
    fig.savefig(buf, format='png', dpi=180, bbox_inches='tight', facecolor='white')
    buf.seek(0); plt.close(fig)
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.add_run().add_picture(buf, width=Inches(width))
    c = doc.add_paragraph(caption); c.alignment = WD_ALIGN_PARAGRAPH.CENTER
    c.runs[0].font.size = Pt(8.5); c.runs[0].font.italic = True; c.runs[0].font.color.rgb = GRAY


def _init_doc():
    doc = Document()
    s = doc.styles['Normal']; s.font.name = FONT; s.font.size = Pt(10)
    for lv in [1, 2, 3]:
        h = doc.styles[f'Heading {lv}']; h.font.name = FONT; h.font.color.rgb = NAVY

    # ── Footer disclaimer on all pages ──
    section = doc.sections[0]
    footer = section.footer
    footer.is_linked_to_previous = False
    fp = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
    fp.alignment = WD_ALIGN_PARAGRAPH.CENTER
    fp.paragraph_format.space_before = Pt(4)
    disclaimer = ("Note: These calculations are valid under the soil conditions as stated herein. "
                  "Engineering responsibility lies with the Engineer-in-Charge. "
                  "This report is generated by Deep Excavation Analysis Tool and must be reviewed "
                  "by a qualified structural engineer before use.")
    run = fp.add_run(disclaimer)
    run.font.name = FONT
    run.font.size = Pt(8)
    run.font.color.rgb = GRAY
    run.italic = True

    return doc


