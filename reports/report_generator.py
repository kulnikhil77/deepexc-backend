def _sec_fe_model(doc, ch, wall_toe, n_anchors, anchor_levels, exc_depth):
    doc.add_heading(f'{ch}. Analysis Method & Finite Element Model', level=1)

    doc.add_heading(f'{ch}.1 Beam-on-Elastic-Foundation Model', level=2)
    _p(doc, "The wall is modelled as a one-dimensional Euler-Bernoulli beam discretised into "
       "finite elements along its length. The soil loading is applied as a distributed pressure "
       "converted to equivalent nodal forces using consistent load vectors.")

    _p(doc, "Key assumptions:", bold=True)
    _p(doc, "1. Plane strain conditions — wall analysed per metre of wall run.")
    _p(doc, "2. Linear-elastic beam elements with constant EI.")
    _p(doc, "3. Small displacement theory (geometric linearity).")
    _p(doc, "4. Rankine earth pressure (smooth wall, horizontal backfill).")
    _p(doc, "5. Active pressure applied as loading; passive pressure as factored resistance below excavation.")

    doc.add_heading(f'{ch}.2 Element Discretisation', level=2)
    n_elem = int(wall_toe / 0.1)
    n_nodes = n_elem + 1
    _p(doc, f"Wall length: {wall_toe:.1f} m, element size: 0.1 m")
    _f(doc, f"Number of elements: {n_elem}")
    _f(doc, f"Number of nodes: {n_nodes}")
    _f(doc, f"DOF per node: 2 (lateral displacement w, rotation theta)")
    _f(doc, f"Total DOF: {n_nodes * 2}")

    doc.add_heading(f'{ch}.3 Boundary Conditions & Supports', level=2)
    _p(doc, "Support conditions:", bold=True)
    supp_rows = []
    for i in range(n_anchors):
        lvl = anchor_levels[i] if i < len(anchor_levels) else 0
        supp_rows.append([f"Anchor {i+1}", f"{lvl:.1f} m", "Pin (w = 0)", "Horizontal reaction"])
