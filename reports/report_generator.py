# PRESSURE DIAGRAM PLOT
# ═══════════════════════════════════════════════════════════════
def _sec_pressure_diagram(doc, ch, layers, gwt_behind, gwt_front, surcharge, exc_depth, wall_toe):
    doc.add_heading(f'{ch}. Earth & Water Pressure Diagrams', level=1)
    _ref(doc, "Ref: IS 9527 Cl. 4.2 — Lateral pressure distribution")

    _p(doc, "The following diagrams show the distribution of active earth pressure, water pressure, "
       "and net pressure along the wall height. Passive pressure is shown below excavation level.")

    gamma_w = 9.81
    # Build pressure profile at 0.25m intervals
    dz = 0.25
    n_pts = int(wall_toe / dz) + 1
    z_arr = [i * dz for i in range(n_pts)]
    pa_arr = []  # active effective
    pp_arr = []  # passive effective
    uw_arr = []  # water pressure (behind)
    uw_front = []  # water pressure (front)
    net_arr = []  # net total

    for z in z_arr:
        # Find layer at this depth
        phi = 30; c = 0; gamma = 18.0
        for L in layers:
            if L.get('top', 0) <= z < L.get('bottom', 99):
                phi = L.get('phi', 30); c = L.get('c', 0); gamma = L.get('gamma', 18.0)
                break

        Ka = math.tan(math.radians(45 - phi/2))**2
        Kp = math.tan(math.radians(45 + phi/2))**2

        # Vertical effective stress
        sv = surcharge
        cum = 0
        for L in layers:
            lt = L.get('top', cum); lb = L.get('bottom', cum + 3)
            g = L.get('gamma', 18.0)
            if z <= lt: break
            z_in = min(z, lb) - lt
            if lb > gwt_behind:
                above_wt = max(0, min(z_in, gwt_behind - lt))
                below_wt = z_in - above_wt
                sv += g * above_wt + (g - gamma_w) * below_wt
            else:
                sv += g * z_in
            cum = lb

        # Active
        sig_ah = max(0, Ka * sv - 2 * c * math.sqrt(Ka))
        # Water behind wall
        u_behind = max(0, gamma_w * (z - gwt_behind))
        # Total active = effective + water
        pa_total = sig_ah + u_behind

        # Passive (only below excavation)
        if z > exc_depth:
            sv_front = 0
            depth_below_exc = z - exc_depth
            for L in layers:
                lt = L.get('top', 0); lb = L.get('bottom', 99)
                if lt >= z or lb <= exc_depth: continue
                g = L.get('gamma', 18.0)
                z_start = max(exc_depth, lt)
                z_end = min(z, lb)
                sv_front += g * (z_end - z_start)
            phi_front = phi; c_front = c
            Kp_f = math.tan(math.radians(45 + phi_front/2))**2
            sig_ph = Kp_f * sv_front + 2 * c_front * math.sqrt(Kp_f)
            u_front = max(0, gamma_w * (z - gwt_front))
            pp_total = sig_ph + u_front
        else:
            pp_total = 0
            u_front = 0

        pa_arr.append(pa_total)
        pp_arr.append(pp_total)
        uw_arr.append(u_behind)
        uw_front.append(u_front)
        net_arr.append(pa_total - pp_total / 1.5)  # factored passive

    # Plot
    fig, (ax1, ax2, ax3) = plt.subplots(1, 3, figsize=(14, 8), sharey=True)

    # Active pressure
    ax1.plot(pa_arr, z_arr, '#1A5276', lw=2, label='Active (total)')
    ax1.plot(uw_arr, z_arr, '#2E86C1', lw=1.5, ls='--', label='Water (behind)')
    ax1.fill_betweenx(z_arr, 0, pa_arr, alpha=0.1, color='#1A5276')
    ax1.axhline(exc_depth, color='#C0392B', ls='--', lw=1)
    ax1.axvline(0, color='black', lw=0.5)
    ax1.set_xlabel('Pressure (kPa)', fontsize=12)
    ax1.set_ylabel('Depth (m)', fontsize=12)
    ax1.set_title('Active Pressure', fontweight='bold', fontsize=14)
    ax1.invert_yaxis(); ax1.legend(fontsize=9); ax1.grid(True, alpha=0.2)

    # Passive pressure
    ax2.plot(pp_arr, z_arr, '#27AE60', lw=2, label='Passive (total)')
    ax2.plot(uw_front, z_arr, '#2E86C1', lw=1.5, ls='--', label='Water (front)')
    ax2.fill_betweenx(z_arr, 0, pp_arr, alpha=0.1, color='#27AE60')
    ax2.axhline(exc_depth, color='#C0392B', ls='--', lw=1)
    ax2.axvline(0, color='black', lw=0.5)
    ax2.set_xlabel('Pressure (kPa)', fontsize=12)
    ax2.set_title('Passive Pressure', fontweight='bold', fontsize=14)
    ax2.legend(fontsize=9); ax2.grid(True, alpha=0.2)

    # Net pressure
    ax3.plot(net_arr, z_arr, '#8E44AD', lw=2, label='Net (factored)')
    ax3.fill_betweenx(z_arr, 0, net_arr, alpha=0.1, color='#8E44AD')
    ax3.axhline(exc_depth, color='#C0392B', ls='--', lw=1)
    ax3.axvline(0, color='black', lw=0.5)
    ax3.set_xlabel('Pressure (kPa)', fontsize=12)
    ax3.set_title('Net Pressure (Design)', fontweight='bold', fontsize=14)
    ax3.legend(fontsize=9); ax3.grid(True, alpha=0.2)

    fig.suptitle('Lateral Pressure Distribution', fontweight='bold', fontsize=15, y=0.99)
    try:
        fig.tight_layout(rect=[0, 0, 1, 0.96])
    except Exception:
        pass
    _fig(doc, fig, "Figure: Active, Passive and Net Pressure Distribution", 6.5)

    # Tabulate key values
    doc.add_heading(f'{ch}.1 Pressure Values at Key Depths', level=2)
    tab_rows = []
    for i in range(0, len(z_arr), max(1, len(z_arr)//20)):
        z = z_arr[i]
        zone = "Above exc." if z < exc_depth else "Embedment"
        tab_rows.append([f"{z:.2f}", f"{pa_arr[i]:.1f}", f"{pp_arr[i]:.1f}",
                        f"{uw_arr[i]:.1f}", f"{net_arr[i]:.1f}", zone])
